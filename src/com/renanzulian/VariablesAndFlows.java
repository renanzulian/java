public class VariablesAndFlows {
    public static void main(String[] args) {
        int age = 24;
        long bornAge = 1996L;
        float height = 1.87f;
        double weight = 86.19684d;
        char gender = 'M';
        boolean active = true;

        if (age >= 18) {
            System.out.println("You are an adult");
        } else {
            System.out.println("You aren't an adult");
        }

    }
}
